package findlowest;

/**
 *
 * @author Richard Foley
 */
public class FindLowest {

    public int findLowest(int [] values){
        int lowest = values[0];
        int i=1;
        while (values[i] != -99){
            if (values[i] < lowest)
                lowest = values[i];
            i++;
        }
        return lowest;
    }
}
